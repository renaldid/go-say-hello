package go_say_hello

func SayHello(nama string) string {
	return "Hello" + nama
}

func Kali(a int, b int) int {
	return a * b
}

func Tambah(a int, b int) int {
	return a + b
}

func Kurang(a int, b int) int {
	return a - b
}
func Bagi(a int, b int) int {
	return a / b
}

func nama(kucing string) string {
	return "nama kucing" + kucing
}
